    {
        $search = $request->input('search');
        $category = Category::where('name', $id)->get()->first(); 
        $data['products'] = Product::where('category_name', $category->name)->
        search($search)->get();
        if(empty($data['products'])){
            $data['products'] = array();
        }
        return view('front.category.detail')->with('data',$data);
    }

